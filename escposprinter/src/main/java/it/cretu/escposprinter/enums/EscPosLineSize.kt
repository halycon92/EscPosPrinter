package it.cretu.escposprinter.enums

/**
 * Provides sizing capabilities for Text in EscPos Line
 */
enum class EscPosLineSize {
    /**
     * Regular Text Size
     */
    REGULAR,

    /**
     * Text Will have Double Height
     */
    DOUBLE_HEIGHT,

    /**
     * Text will have double width
     */
    DOUBLE_WIDTH,

    /**
     * Text will have both double width and height
     */
    DOUBLE_WIDTH_HEIGHT
}